# 18. Leia o valor do raio de uma circunferência, calcule e escreva seu comprimento.(c = 2 * p * r)

# Entrada
raio = float(input('Digite o raio da circuferência em cm:'))

# Processamento
comprimento = 2 * 3.14 * raio

# Saída
print(f'A Circuferência de raio {raio}cm tem comprimento de {comprimento:.1f}cm2')
