# 17. Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)

# Entrada
base = float(input('Digite a base do retângulo em m: '))
altura = float(input('Digite a altura do retângulo em m: '))

# Processamento
area = base * altura

# Saída
print(f'O Retângulo de base {base} m e altura {altura}m tem área de {area:.1f}m2')
