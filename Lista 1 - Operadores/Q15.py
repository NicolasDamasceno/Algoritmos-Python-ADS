# 15. Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)

# Entrada
base = float(input('Digite a base de um triângulo em m: '))
altura = float(input('Digite a altura do triângulo em m: '))

# Processamento
area = (base * altura) / 2

# Saída
print(f'O Triângulo de base {base}m e altura {altura}m tem área {area}m2')